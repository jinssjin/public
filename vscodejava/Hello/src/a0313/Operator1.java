package a0313;

public class Operator1 {
    public static void main(String[] args) {
        // 변수 초기화
        int a = 5;
        int b = 2;

        // 덧셈
        int sum = a + b;
        System.out.println("a + b = "+sum);  // 출력

        int c = 8;
        int d = 4;

        int diff = a - b;
        System.out.println("a - b = "+diff);

        int e = 3;
        int f = 4;

        int multi = e * f;
        System.out.println("e * f = "+multi);

        int g = 6;
        int h = 3;

        int div = g / h;
        System.out.println("g / h = "+div);

        int mod = a % b;
        System.out.println("나머지는 "+mod);

        
    
    }
}
