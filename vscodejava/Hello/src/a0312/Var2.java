package a0312;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수선언
        a = 20;  // 변수만 바꾸면 출력도 바뀜
        // a를 선언한 변수에 처음으로 값을 대입하는 것을 변수의 초기화라고 한다
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
        // 변수 : 변할 수 있는 수, a값은 언제든 변경 가능
    }
    
}
