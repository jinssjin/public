package a0312;

public class Var4 {
    public static void main(String[] args) {
        // 변수 선언과 초기화 각각 따로
        int a;
        a = 1;
        System.out.println(a);

        // 변수 선언과 초기화를 한번에
        int b = 2;
        System.out.println(b);

        int c = 3, d =4;  // 여러변수 선언과 초기화를 한번에
        System.out.println(c);
        System.out.println(d);

    }
}
